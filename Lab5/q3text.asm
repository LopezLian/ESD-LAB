	AREA RESET, DATA, READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	MOV R4, #0 ;R4==1 if element found
	MOV R6, #10;Counter
	LDR R0, =N
	LDR R2, =KEY
	LDR R2, [R2]
UP	
	LDR R1,[R0],#4
	CMP R1, R2
	ADDEQ R4, #1
	SUB R6, #1
	CMP R6, #0
	BNE UP

	LDR R5, =DST
	STR R4, [R5]
STOP 
	B STOP
N DCD 0X2A, 0x12345678, 0x34534554, 0xABCDEF12, 0x00011122, 0x32, 0x69696969, 0x546AF12E, 0x324, 0x00000EEE
KEY DCD 0x324
	AREA mydata, DATA, READWRITE
DST DCD 0
	END